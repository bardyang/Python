#复数的数学运算
#complex(real,imag)
>>>a = complex(2,4)
>>>b = 3 - 5j
>>>a
(2+4j)
>>>b
(3-5j)

>>>a.real
2.0
>>>a.imag
4.0
>>>a.conjugate() #共轭复数
(2-4j)

#常见的数学运算
>>>a + b
(5-1j)
>>>a * b
(26 + 2j)
>>>a/b
(-0.4117647058823529+0.6470588235294118j)
>>>abs(a)
4.472135955499958

